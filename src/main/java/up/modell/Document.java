package up.modell;

public class Document {
	private int docId;
	private int docName;
	private int docDescr;
	private String docUrl;
	
	
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	public int getDocName() {
		return docName;
	}
	public void setDocName(int docName) {
		this.docName = docName;
	}
	public int getDocDescr() {
		return docDescr;
	}
	public void setDocDescr(int docDescr) {
		this.docDescr = docDescr;
	}
	public String getDocUrl() {
		return docUrl;
	}
	public void setDocUrl(String docUrl) {
		this.docUrl = docUrl;
	}

}
